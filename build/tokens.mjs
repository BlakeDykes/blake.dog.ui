/**
 * Shared layered-token build for the @blakedykes game-UI packages.
 *
 * Emits a package's `_tokens.scss` inside the fixed cascade
 *
 *     @layer structural, identity, brand, invariants;
 *
 * from a declarative list of blocks. Each block is either token-driven (a
 * Style Dictionary pass over some token files, wrapped in its layer) or a raw
 * CSS string (for hand-written invariants floors). Precedence is by layer
 * order, never selector specificity.
 *
 * This is the de-risked replacement for the per-package string-assembly: one
 * helper, thin per-package configs, plus two guard assertions:
 *   - identity blocks must keep their `var()` alias chain (the filter +
 *     outputReferences behaviour that could silently flatten to literals);
 *   - every contract token must be populated by the identity layer.
 */
import StyleDictionary from "style-dictionary";
import { readFile, writeFile, rm, mkdir } from "node:fs/promises";
import path from "node:path";

const HEADER =
  "/**\n" +
  " * Do not edit directly — generated by the shared @blakedykes token build.\n" +
  " * Cascade: @layer structural < identity < brand < invariants.\n" +
  " */\n";

const stripHeader = (s) => s.replace(/^\/\*\*[\s\S]*?\*\/\s*/, "").trimEnd();
const indent = (s) =>
  s
    .split("\n")
    .map((line) => (line.length ? `  ${line}` : line))
    .join("\n");
const layerBlock = (name, body) => `@layer ${name} {\n${indent(body)}\n}\n`;

// Collect the kebab-cased custom-property names a DTCG tree would emit.
function collectNames(node, prefix, out) {
  for (const [key, value] of Object.entries(node)) {
    if (key.startsWith("$")) continue;
    if (value && typeof value === "object") {
      if ("$value" in value) out.push([...prefix, key].join("-"));
      else collectNames(value, [...prefix, key], out);
    }
  }
}

// Run one Style Dictionary CSS pass to a tmp file; return its body.
async function emitBlockCss(block, outDir, index) {
  const tmpName = `.tmp-tokens-${index}.scss`;
  const sd = new StyleDictionary({
    source: block.sources,
    platforms: {
      css: {
        transforms: ["name/kebab"],
        buildPath: outDir.endsWith("/") ? outDir : `${outDir}/`,
        files: [
          {
            destination: tmpName,
            format: "css/variables",
            filter: block.filter,
            options: {
              selector: block.selector, // undefined -> :root
              outputReferences: block.outputReferences ?? false,
            },
          },
        ],
      },
    },
  });
  await sd.buildAllPlatforms();
  const tmpPath = path.join(outDir, tmpName);
  const body = stripHeader(await readFile(tmpPath, "utf8"));
  await rm(tmpPath);
  return body;
}

/**
 * @param {object}   opts
 * @param {string}   opts.outFile       absolute/relative path to the _tokens.scss to write
 * @param {Array}    opts.blocks        ordered blocks: { layer, selector?, sources?, filter?, outputReferences?, raw? }
 * @param {object}  [opts.assert]       { identityKeepsRefs?: boolean, contractFrom?: string }
 * @param {boolean} [opts.throwOnError] throw (vs. process.exit) when assertions fail — set in watch/dev so the
 *                                      dev server reports the error and keeps watching instead of dying.
 */
export async function buildLayeredTokens({
  outFile,
  blocks,
  assert,
  throwOnError = false,
}) {
  const outDir = path.dirname(outFile);
  await mkdir(outDir, { recursive: true });

  const rendered = [];
  for (let i = 0; i < blocks.length; i++) {
    const block = blocks[i];
    const body =
      block.raw != null
        ? block.raw.trim()
        : await emitBlockCss(block, outDir, i);
    rendered.push({ block, body });
  }

  const out =
    HEADER +
    "\n@layer structural, identity, brand, invariants;\n\n" +
    rendered.map(({ block, body }) => layerBlock(block.layer, body)).join("\n");

  // Write only when the content actually changes. In watch mode the generated
  // partial is itself a build input, so an unconditional rewrite would retrigger
  // the bundler on every pass — an infinite rebuild loop.
  const existing = await readFile(outFile, "utf8").catch(() => null);
  const changed = existing !== out;
  if (changed) await writeFile(outFile, out);

  // --- guard assertions ----------------------------------------------------
  const problems = [];

  if (assert?.identityKeepsRefs) {
    for (const { block, body } of rendered) {
      if (
        block.layer === "identity" &&
        (block.outputReferences ?? false) &&
        !body.includes("var(--")
      ) {
        problems.push(
          `identity block ${JSON.stringify(
            block.selector ?? ":root"
          )} flattened every reference to a literal — the outputReferences alias chain regressed`
        );
      }
    }
  }

  if (assert?.contractFrom) {
    const tree = JSON.parse(await readFile(assert.contractFrom, "utf8"));
    const required = [];
    collectNames(tree, [], required);
    const identityBody = rendered
      .filter((r) => r.block.layer === "identity")
      .map((r) => r.body)
      .join("\n");
    for (const name of required) {
      if (!new RegExp(`--${name}\\s*:`).test(identityBody)) {
        problems.push(
          `contract token --${name} is not populated by the identity layer`
        );
      }
    }
  }

  if (problems.length) {
    const message =
      "Token build assertions failed:\n" +
      problems.map((p) => `  - ${p}`).join("\n");
    if (throwOnError) throw new Error(message);
    console.error(message);
    process.exit(1);
  }

  console.log(`Token build ${changed ? "complete" : "up to date"}: ${outFile}`);
  return changed;
}
